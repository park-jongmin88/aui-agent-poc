"""
==============================================================================
 AI-Studio  |  LLM 통신 단독 테스트  (test_llm.py)
==============================================================================

 [이 파일이 하는 일]
   서빙/등록과 상관없이, LLM(Qwen) 과의 통신만 따로 확인한다.
   agent.py 의 call_llm() 을 직접 불러서 답변이 오는지만 본다.

   서빙에서 문제가 생겼을 때 "LLM 연결 문제인가, 그 다음 단계 문제인가" 를
   가장 빠르게 가려내는 용도다.

 [실행]
   python agent/test_llm.py

 ----------------------------------------------------------------------------
 [읽는 순서 — 위에서 아래로]
   [0] conn   : 테스트할 LLM 연결 정보 (실제 값으로 채움)
   [1] msg    : 보낼 테스트 메시지
   [2] 실행   : call_llm() 호출 → 성공/실패 출력
 ----------------------------------------------------------------------------

 [결과 판단]
   답변이 출력되면        → LLM 통신은 정상.
                            (서빙이 안 되면 문제는 그 다음 단계 = 응답 가공/등록)
   연결/타임아웃 오류면   → LLM 통신 자체가 문제.
                            (base_url / 방화벽 / 모델명 확인)
==============================================================================
"""

# agent.py 의 call_llm 을 그대로 가져와 테스트한다 (같은 폴더에서 실행)
from agent import call_llm


# =============================================================================
# [0]  LLM 연결 정보  ← 실제 값으로 채운다
# -----------------------------------------------------------------------------
#   agent.py 의 LLM_CONN 과 같은 형태. 여기선 테스트용으로 직접 적는다.
# =============================================================================
conn = {
    "base_url":    "http://qwen.internal:8000/v1",   # LLM 엔드포인트 (실제 값)
    "api_key":     "not-needed",                     # 인증 키 (없으면 그대로)
    "model":       "qwen2.5-7b-instruct",            # 모델명
    "temperature": 0.2,
}

# =============================================================================
# [1]  보낼 테스트 메시지 (OpenAI chat 형식)
# =============================================================================
msg = [{"role": "user", "content": "안녕"}]


# =============================================================================
# [2]  실행 — call_llm() 호출 후 성공/실패 출력
# =============================================================================
print("=" * 50)
print(" LLM 통신 테스트")
print("=" * 50)
print(f"  base_url : {conn['base_url']}")
print(f"  model    : {conn['model']}")
print("-" * 50)

try:
    answer = call_llm(msg, conn)
    print("  [성공] LLM 응답:")
    print(f"  {answer}")
except Exception as e:
    # 실패 시 예외 타입과 메시지를 보여줘 원인 파악을 돕는다
    print(f"  [실패] {type(e).__name__}: {e}")

print("=" * 50)
